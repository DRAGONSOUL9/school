void	ft_rev_int_tab(int *tab, int size)
{
	int	f;
	int	l;
	int	tmp;

	f = 0;
	l = size - 1;
	while (f < (size / 2))
	{
		tmp = tab[f];
		tab[f] = tab[l];
		tab[l] = tmp;
		f++;
		l--;
	}
}
