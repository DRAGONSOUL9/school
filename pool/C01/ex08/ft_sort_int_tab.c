void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	tmp;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		if (tab[i] > tab[j] && j < size)
		{
			tmp = tab[i];
			tab[i] = tab[j];
			tab[j] = tmp;
			i = -1;
		}
		i++;
	}
}
