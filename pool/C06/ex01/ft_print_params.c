#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	a;

	a = 1;
	while (a < ac)
	{
		i = 0;
		while (av[a][i])
		{
			write (1, &av[a][i], 1);
			i++;
		}
		write (1, "\n", 1);
		a++;
	}
	return (0);
}
