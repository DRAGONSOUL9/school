#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	v;

	v = 1;
	while (v < ac)
	{
		i = 0;
		while (av[v][i])
		{
			write (1, &av[v][i], 1);
			i++;
		}
		write (1, "\n", 1);
		v++;
	}
	return (0);
}
