#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	v;

	v = ac - 1;
	while (v > 0)
	{
		i = 0;
		while (av[v][i])
			write (1, &av[v][i++], 1);
		ac--;
	}
	return (0);
}
